num1 = 10
num2 = 4

print("num1 & num2 are:", num1 , num2)
print("num1 | num2 are:", num1 | num2) #OR operator
print("num1 & num2 are:", num1 & num2) #AND operator
print("num1 ^ num2 are:", num1 ^ num2) #XOR operator
print("num1 << num2 are:", num1 << num2) #left shift operator
print("num1 >> num2 are:", num1 >> num2) #right shift operator
print("~num1 are:", ~num1) #negation operator
print("~num2 are:", ~num2)